get_mode_value() {
    local mode="$1"
    local key="$2"
    local block

    if [ ! -f "$MODE_CONFIG_FILE" ]; then
        echo ""
        return 1
    fi

    block=$(awk -v mode="$mode" '
        $1 == "modes:" {in_modes=1; next}
        in_modes && $1 == mode":" {in_mode=1; next}
        in_modes && in_mode && $1 ~ "^[a-zA-Z0-9_-]+:" && $2 == "" {exit}
        in_mode {print}
    ' "$MODE_CONFIG_FILE")

    if [ -z "$block" ]; then
        echo ""
        return 1
    fi

    echo "$block" | awk -v key="$key" '$1 == key":" {print $2; exit}'
}

require_mode_value() {
    local mode="$1"
    local key="$2"
    local value

    value=$(get_mode_value "$mode" "$key")
    if [ -z "$value" ]; then
        echo "ã‚¨ãƒ©ãƒ¼: ${MODE_CONFIG_FILE} ã® ${mode}.${key} ãŒè¦‹ã¤ã‹ã‚Šã¾ã›ã‚“"
        exit 1
    fi
    echo "$value"
}

